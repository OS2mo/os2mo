from datetime import datetime
from uuid import UUID

import pytest
from fastramqpi.pytest_util import retrying
from httpx import AsyncClient
from structlog.testing import capture_logs

from mo_ldap_import_export.autogenerated_graphql_client import GraphQLClient
from mo_ldap_import_export.autogenerated_graphql_client.input_types import (
    EmployeeCreateInput,
)
from mo_ldap_import_export.autogenerated_graphql_client.input_types import (
    EngagementCreateInput,
)
from mo_ldap_import_export.autogenerated_graphql_client.input_types import (
    OrganisationUnitCreateInput,
)


@pytest.mark.integration_test
@pytest.mark.envvar(
    {
        "LISTEN_TO_CHANGES_IN_MO": "True",
        "LISTEN_TO_CHANGES_IN_LDAP": "False",
    }
)
@pytest.mark.parametrize(
    "e_start,e_end,o_start,o_end",
    [
        # Engagement current, org-unit current
        (
            datetime(1970, 1, 1),
            None,
            datetime(1970, 1, 1),
            None,
        ),
        # No case with engagement to=None and ended org-unit as MO disallows this
        # with ErrorCodes.V_DATE_OUTSIDE_ORG_UNIT_RANGE
        (
            datetime(1970, 1, 1),
            datetime(3000, 1, 1),
            datetime(1970, 1, 1),
            None,
        ),
        (
            datetime(1970, 1, 1),
            datetime(3000, 1, 1),
            datetime(1970, 1, 1),
            datetime(3000, 1, 1),
        ),
        # Engagement past, org-unit current
        (
            datetime(1970, 1, 1),
            datetime(1980, 1, 1),
            datetime(1970, 1, 1),
            None,
        ),
        (
            datetime(1970, 1, 1),
            datetime(1980, 1, 1),
            datetime(1970, 1, 1),
            datetime(3000, 1, 1),
        ),
        # Engagement past, org-unit past
        (
            datetime(1970, 1, 1),
            datetime(1980, 1, 1),
            datetime(1970, 1, 1),
            datetime(1980, 1, 1),
        ),
        # Engagement future, org-unit current
        (
            datetime(2900, 1, 1),
            None,
            datetime(1970, 1, 1),
            None,
        ),
        (
            datetime(2900, 1, 1),
            datetime(3000, 1, 1),
            datetime(1970, 1, 1),
            None,
        ),
        (
            datetime(2900, 1, 1),
            datetime(3000, 1, 1),
            datetime(1970, 1, 1),
            datetime(3000, 1, 1),
        ),
        # Engagement future, org-unit future
        (
            datetime(2900, 1, 1),
            datetime(3000, 1, 1),
            datetime(2900, 1, 1),
            None,
        ),
        (
            datetime(2900, 1, 1),
            datetime(3000, 1, 1),
            datetime(2900, 1, 1),
            datetime(3000, 1, 1),
        ),
    ],
)
async def test_orgunit2engagement2person(
    test_client: AsyncClient,
    graphql_client: GraphQLClient,
    mo_person: UUID,
    afdeling: UUID,
    ansat: UUID,
    jurist: UUID,
    primary: UUID,
    e_start: datetime | None,
    e_end: datetime | None,
    o_start: datetime | None,
    o_end: datetime | None,
) -> None:
    org_unit = await graphql_client.org_unit_create(
        input=OrganisationUnitCreateInput(
            name="org_unit",
            org_unit_type=afdeling,
            validity={"from": o_start, "to": o_end},
        )
    )

    await graphql_client.engagement_create(
        input=EngagementCreateInput(
            user_key="engagement",
            person=mo_person,
            org_unit=org_unit.uuid,
            engagement_type=ansat,
            job_function=jurist,
            primary=primary,
            validity={"from": e_start, "to": e_end},
        )
    )

    with capture_logs() as cap_logs:
        result = await test_client.post(
            "/mo2ldap/org_unit", json={"subject": str(org_unit.uuid), "priority": 1}
        )
        result.raise_for_status()

        async for attempt in retrying():
            with attempt:
                assert {
                    "log_level": "info",
                    "event": "Registered change in a person",
                    "object_uuid": mo_person,
                } in cap_logs


