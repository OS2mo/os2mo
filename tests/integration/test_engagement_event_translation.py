async def test_engagement2person_change_person(
    test_client: AsyncClient,
    graphql_client: GraphQLClient,
    mo_org_unit: UUID,
    ansat: UUID,
    jurist: UUID,
    primary: UUID,
) -> None:
    person1 = await graphql_client.person_create(
        input=EmployeeCreateInput(
            given_name="Aage",
            surname="Aagaard",
            cpr_number="0101010123",
        )
    )

    person2 = await graphql_client.person_create(
        input=EmployeeCreateInput(
            given_name="Betina",
            surname="Bundgaard",
            cpr_number="0101011234",
        )
    )

    engagement = await graphql_client.engagement_create(
        input=EngagementCreateInput(
            user_key="engagement",
            person=person1.uuid,
            org_unit=mo_org_unit,
            engagement_type=ansat,
            job_function=jurist,
            primary=primary,
            validity={"from": datetime(2000, 1, 1), "to": datetime(2010, 1, 1)},
        )
    )
    await graphql_client.engagement_update(
        input=EngagementUpdateInput(
            uuid=engagement.uuid,
            person=person2.uuid,
            validity={"from": datetime(3000, 1, 1), "to": datetime(3010, 1, 1)},
        )
    )

    with capture_logs() as cap_logs:
        result = await test_client.post(
            "/mo2ldap/engagement", json={"subject": str(engagement.uuid), "priority": 1}
        )
        result.raise_for_status()

        # Check that we sent the event to MO
        async for attempt in retrying():
            with attempt:
                assert {
                    "log_level": "info",
                    "event": "Registered change in a person",
                    "object_uuid": person1.uuid,
                } in cap_logs
                assert {
                    "log_level": "info",
                    "event": "Registered change in a person",
                    "object_uuid": person2.uuid,
                } in cap_logs
