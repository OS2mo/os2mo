@pytest.mark.integration_test
@pytest.mark.envvar(
    {
        "LISTEN_TO_CHANGES_IN_MO": "False",
        "LISTEN_TO_CHANGES_IN_LDAP": "False",
    }
)
@pytest.mark.usefixtures("test_client")
async def test_load_primary_engagement(
    graphql_client: GraphQLClient,
    context: Context,
    mo_person: UUID,
    mo_org_unit: UUID,
    ansat: UUID,
    jurist: UUID,
    primary: UUID,
) -> None:
    title = "create"
    await graphql_client.engagement_create(
        input=EngagementCreateInput(
            user_key=title,
            person=mo_person,
            org_unit=mo_org_unit,
            engagement_type=ansat,
            job_function=jurist,
            primary=primary,
            fraction=100,
            extension_1=title,
            validity={"from": "2001-02-03T04:05:06Z", "to": "2002-03-04T05:06:07Z"},
        )
    )

    dataloader = context["user_context"]["dataloader"]

    result = await load_primary_engagement_recalculated(
        dataloader.moapi,
        mo_person,
        return_terminated=False,
    )
    assert result is None

    result = await load_primary_engagement_recalculated(
        dataloader.moapi,
        mo_person,
        return_terminated=True,
    )
    assert result is not None
    assert result.dict(exclude_none=True) == {
        "engagement_type": ansat,
        "extension_1": title,
        "job_function": jurist,
        "org_unit": mo_org_unit,
        "person": mo_person,
        "primary": primary,
        "user_key": title,
        "uuid": ANY,
        "validity": {
            "start": datetime(2001, 2, 3, 0, 0, tzinfo=MO_TZ),
            "end": datetime(2002, 3, 4, 0, 0, tzinfo=MO_TZ),
        },
        "fraction": 100,
    }


