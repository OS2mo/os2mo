from datetime import datetime
from uuid import UUID

import pytest
from fastramqpi.pytest_util import retrying
from httpx import AsyncClient
from structlog.testing import capture_logs

from mo_ldap_import_export.autogenerated_graphql_client import GraphQLClient
from mo_ldap_import_export.autogenerated_graphql_client.input_types import (
    EmployeeCreateInput,
)
from mo_ldap_import_export.autogenerated_graphql_client.input_types import (
    ITUserCreateInput,
)
from mo_ldap_import_export.autogenerated_graphql_client.input_types import (
    ITUserUpdateInput,
)
from mo_ldap_import_export.autogenerated_graphql_client.input_types import (
    OrganisationUnitCreateInput,
)


@pytest.mark.integration_test
@pytest.mark.envvar(
    {
        "LISTEN_TO_CHANGES_IN_MO": "True",
        "LISTEN_TO_CHANGES_IN_LDAP": "False",
    }
)
@pytest.mark.parametrize(
    "start,end",
    [
        # Current
        (datetime(1970, 1, 1), None),
        (datetime(1970, 1, 1), datetime(3000, 1, 1)),
        # Past
        (datetime(1970, 1, 1), datetime(1980, 1, 1)),
        # Future
        (
            datetime(2900, 1, 1),
            datetime(3000, 1, 1),
        ),
    ],
)
async def test_ituser2person(
    test_client: AsyncClient,
    graphql_client: GraphQLClient,
    mo_person: UUID,
    adtitle: UUID,
    start: datetime | None,
    end: datetime | None,
) -> None:
    ituser = await graphql_client.ituser_create(
        input=ITUserCreateInput(
            user_key="ituser",
            person=mo_person,
            itsystem=adtitle,
            validity={"from": start, "to": end},
        )
    )

    with capture_logs() as cap_logs:
        result = await test_client.post(
            "/mo2ldap/ituser", json={"subject": str(ituser.uuid), "priority": 1}
        )
        result.raise_for_status()

        # Check that we sent the event to MO
        async for attempt in retrying():
            with attempt:
                assert {
                    "log_level": "info",
                    "event": "Registered change in a person",
                    "object_uuid": mo_person,
                } in cap_logs


